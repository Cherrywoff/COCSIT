                          <p>The canteen provides <b>affordable, hygienic, and nutritious food</b> to students and staff. It serves meals, snacks, beverages, and sometimes regional cuisines. It also acts as a <b>social space</b> where students relax, interact, and spend leisure time between classes.</p> 
<h4>8. Hostel Facility</h4>
                          <p>Hostels provide safe and comfortable accommodation for outstation students. Facilities include <b>furnished rooms, clean drinking water, mess services, study areas.</b>
Security, wardens, and strict rules ensure discipline and safety. Separate hostels are available for boys and girls.</p> 

<h4>9. Internet & Wi-Fi</h4>
                          <p>College equipped with <b>high-speed internet access across the campus.</b> Students can use it for research, online classes, assignments, and communication.
Secure login systems and firewalls maintain safe usage and protect against cyber threats.</p> 
