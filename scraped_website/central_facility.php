</p>
                          <h4>4. Smart Classrooms</h4>
                          <p><b>Classrooms</b> are equipped with projectors, interactive boards, audio systems, and multimedia tools.
These facilities enable teachers to deliver engaging lectures using presentations, videos, and digital content, making learning more interactive and effective.</p>             

<h4>5. Medical Facility </h4>
                          <p>The campus health center provides <b>basic medical care and first aid services</b> to students and staff. 
Tie-ups with nearby hospitals ensure quick medical assistance when needed.</p>             

